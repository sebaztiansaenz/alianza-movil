    // On page load action.
    SchedulerBinding.instance.addPostFrameCallback((_) async {
      _model.transaction = await queryTransactionsRecordOnce(
        queryBuilder: (transactionsRecord) => transactionsRecord.where(
          'referenceId',
          isEqualTo: FFAppState().paymentReference,
        ),
        singleRecord: true,
      ).then((s) => s.firstOrNull);

      safeSetState(() {});
      try {
        final result =
            await FirebaseFunctions.instanceFor(region: 'europe-central2')
                .httpsCallable('checkPaymentStatus')
                .call({
          "requestId": _model.transaction!.requestId.toString(),
        });
        _model.cloudFunctionopb = CheckPaymentStatusCloudFunctionCallResponse(
          data: result.data,
          succeeded: true,
          resultAsString: result.data.toString(),
          jsonBody: result.data,
        );
      } on FirebaseFunctionsException catch (error) {
        _model.cloudFunctionopb = CheckPaymentStatusCloudFunctionCallResponse(
          errorCode: error.code,
          succeeded: false,
        );
      }

      if (_model.cloudFunctionopb!.succeeded!) {
        _model.status = _model.cloudFunctionopb?.data;
        safeSetState(() {});

        await _model.transaction!.reference.update(createTransactionsRecordData(
          status: () {
            if (_model.cloudFunctionopb?.data == 'APPROVED') {
              return 'APPROVED';
            } else if (_model.cloudFunctionopb?.data == 'PENDING') {
              return 'PENDING';
            } else {
              return 'REJECTED';
            }
          }(),
        ));
      } else {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text(
              'Failed to get status.',
              style: TextStyle(
                color: FlutterFlowTheme.of(context).primaryText,
              ),
            ),
            duration: Duration(milliseconds: 4000),
            backgroundColor: FlutterFlowTheme.of(context).error,
          ),
        );
      }
    });

    WidgetsBinding.instance.addPostFrameCallback((_) => safeSetState(() {}));
  }

